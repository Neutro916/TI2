# Step 2: Create scheduled task for auto-start
Write-Info "Creating Windows Scheduled Task for auto-start..."

$TaskAction = New-ScheduledTaskAction -Execute "PowerShell.exe" `
    -Argument "-NoProfile -WindowStyle Hidden -File `"$StartupScriptPath`"" `
    -WorkingDirectory $ProjectRoot

$TaskTrigger = New-ScheduledTaskTrigger -AtLogOn

$TaskPrincipal = New-ScheduledTaskPrincipal -UserId "SYSTEM" -LogonType ServiceAccount -RunLevel Highest

$TaskSettings = New-ScheduledTaskSettingsSet `
    -AllowStartIfOnBatteries `
    -DontStopIfGoingOnBatteries `
    -StartWhenAvailable `
    -RunOnlyIfNetworkAvailable `
    -WakeToRun `
    -ExecutionTimeLimit (New-TimeSpan -Hours 24) `
    -RestartCount 3 `
    -RestartInterval (New-TimeSpan -Minutes 1)

# Create task
try {
    # Create task folder first
    $null = Register-ScheduledTask `
        -TaskName $TaskName `
        -TaskPath $TaskPath `
        -Action $TaskAction `
        -Trigger $TaskTrigger `
        -Principal $TaskPrincipal `
        -Settings $TaskSettings `
        -Force `
        -ErrorAction Stop
    
    Write-Success "Scheduled task created: $TaskPath$TaskName ✓"
    Write-Host ""
    Write-Info "Task will auto-start T2I-Bot-Skill when you log in to Windows"
} catch {
    Write-Error "Failed to create scheduled task: $_"
    Write-Host ""
    Write-Info "You can manually run the startup script instead: $StartupScriptPath"
}

Write-Host ""

# Step 3: Create startup shortcut in Startup folder
Write-Info "Creating Startup folder shortcut..."

$StartupFolder = [Environment]::GetFolderPath("Startup")
