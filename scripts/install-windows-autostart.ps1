# Start code-server (if installed)
if (Get-Command code-server -ErrorAction SilentlyContinue) {
    Write-Host "[INFO] Starting code-server on port 8080..." -ForegroundColor Yellow
    Start-Process code-server -ArgumentList "--config", "`$env:USERPROFILE\.config\code-server\config.yaml"
}

# Start OpenWebUI (if Docker is available)
if (Get-Command docker -ErrorAction SilentlyContinue) {
    Write-Host "[INFO] Checking Docker containers..." -ForegroundColor Yellow
    docker ps -a | Select-String "openwebui"
}

# Open browser after delay
Start-Sleep -Seconds 10
